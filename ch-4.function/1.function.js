// Using functions, we can execute a block of code in our program repeatedly.

// In JavaScript, we can create a function using the 'function' keyword.

// Example:

// This is called a function declaration
function jsFunction() {
  console.log("js function created");
}

// Function invocation or function call (this executes the code inside the function)
jsFunction();

// Now, function expression:

// A function expression means assigning a function to a variable.

// Example:
let play = function game() {
  console.log("playing cricket");
};

// Function invocation
play();
